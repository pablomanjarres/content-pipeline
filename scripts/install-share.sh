# The shell script that runs when you share files
UPLOAD_SCRIPT='for f in "$@"; do
    if [ -f "$f" ]; then
        DATE=$(date +%Y-%m-%d)
        YEAR=$(date +%G)
        WEEK=$(date +%V)
        WEEK_KEY="${YEAR}-W${WEEK}"
        curl -s -X POST "http://localhost:3010/api/media/upload/${WEEK_KEY}/${DATE}" \
            -F "files=@${f}"
    fi
done
COUNT=$#
osascript -e "display notification \"${COUNT} file(s) uploaded\" with title \"Content Pipeline\""'

# Escape for XML embedding
